	private void writeEnums(BlockField top) {
		//first make a list of all unique enums
		ArrayList<EnumField> es = new ArrayList<EnumField>();
		top.scanThroughBaseFields((f, p) -> {
			if(f instanceof EnumField) {
				EnumField e = (EnumField)f;
				boolean found = false;
				//check that this new one isn't the same as an existing one
				for(EnumField ef : es) {
					if(ef.getTypeName().equals(e)) {
						found = true;
						break;
					}
				}
				if(!found) {
					es.add(e);
				}
			}
		});	
		for(EnumField ef : es) {
			addDocComment(ef.getComment());
			add("typedef enum {");
			
			indent();
			for(NameValue nv : ef.getNameValues()) {
				addNewLine();
				add(nv.getName().toUpperSnake());
				add(" = " + nv.getValue());
				String c = nv.getComment();
				if(c != null && !c.isBlank()) {
					add("// "+c);
				}
				
				
			}
			outdent();
			addNewLine();
			add("} "+ef.getTypeName().toUpperCamel());
			addNewLine();
			addNewLine();

		}
	}

	private void writeDefines(BlockField top) {
		top.scanThroughBaseFields((f, p) -> {
			if(f.getName() != null) {
				boolean multiLine = f.getComment().split("\\R").length > 1 ;
				String c = "";
				if(multiLine) {
					addBlockComment(f.getComment());
				} else {
					String s = f.getComment();
					if(!s.isBlank()) {
						c = "    //"+f.getComment();
					}
				}
				
				writeDefine(f.getName().addPrefix(top.getName()).addSuffix("field"), ""+f.getIndex(),c);
			}
		});	
	}

	
	

	private void writeDefine(FieldName name, String value, String comment) {
		addIndent();
		add("#define ");
		add(name.toUpperSnake());
		add(" (" + value + ")");
		if(!comment.isEmpty()) {
			add(comment);
		}
		addNewLine();
		
	}


	private void addBaseFields(BlockField top) {
		top.scanThroughBaseFields((f, p) -> {
			if(f instanceof BoolFieldField) {
				addBoolGetterPrototype((BoolFieldField)f, top);
			} else if(f instanceof CompoundField) {
				addCompoundGetterPrototype((CompoundField)f, top);
			} else {
				addBaseGetterPrototype(f,p);
			}
		});
	}
	private void addCompoundGetterPrototype(CompoundField f, BlockField top) {
		// TODO Auto-generated method stub
		
	}

	private void addBoolGetterPrototype(BoolFieldField f, BlockField top) {
		for(BoolField b : f.getBoolFields()) {
			addBaseGetterPrototype(b, top);
		}
		
	}

	private void addBaseGetterPrototype(BaseField f, ParentField p) {
		if(f.getName() != null) {
			String c = f.getName().toLowerCamel()+" field of the " + p.getName().toLowerCamel() + " " + p.getTypeName().toUpperCamel()+ "\n"+f.getComment();
			String rt = getBaseType(f.getType());
			String function = f.getName().addPrefix(p.getName()).addSuffix("field").toUpperCamel();
			String paramType = "BlueberryBlock*";
			String paramName = p.getName().toLowerCamel();
			addDocComment("gets the "+ c);
			addLine(rt + " get" + function + "(" + paramType + " " + paramName + ");");
			addDocComment("sets the "+ c);
			addLine("void" + " set" + function + "(" + paramType + " " + paramName + ", " + rt + " " + f.getName().toLowerCamel()+");");
		}
		
		
		
		
	}
	private String getBaseType(Type t) {
		String rt = "";
		switch(t) {
		case COMPOUND:
		case ARRAY:
		case BLOCK:
			break;
		case BOOL:
			rt = "bool";
			break;
		case BOOLFIELD:
			rt = "uint8_t";
			break;
		case FLOAT32:
			rt = "float";
			break;
		case INT16:
			rt = "int16_t";
			break;
		case INT32:
			rt = "int32_t";
			break;
		case INT8:
			rt = "int8_t";
			break;
		case UINT16:
			rt = "uint16_t";
			break;
		case UINT32:
			rt = "uint32_t";
			break;
		case UINT8:
			rt = "uint8_t";
			break;
		default:
			break;
		
		}
		return rt;
	}
	

